public class MyData {
    private String dat,week;

    public MyData(String dat, String week) {
        this.dat = dat;
        this.week = week;
    }

    public String getDat() {
        return dat;
    }

    public void setDat(String dat) {
        this.dat = dat;
    }

    public String getWeek() {
        return week;
    }

    public void setWeek(String week) {
        this.week = week;
    }
}
